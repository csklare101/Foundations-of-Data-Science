student = 90;

if(student >= 90){
  letterGrade = 'A'
} else if(student >= 80){
  letterGrade = 'B'
} else if(student >= 70){
  letterGrade = 'C'
} else if(student >= 60){
  letterGrade = 'D'
} else{
  letterGrade = 'F'
}

paste("Student has an ", letterGrade, "!", " Score was: ", student, sep = )
